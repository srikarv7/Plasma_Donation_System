    private void JComboBoxOrganizationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JComboBoxOrganizationActionPerformed
        Organization organization = (Organization) JComboBoxOrganization.getSelectedItem();
        if (organization != null){
            populateEmployeeComboBox(organization);
            populateRoleComboBox(organization);
        }
        if(String.valueOf(JComboBoxOrganization.getItemAt(JComboBoxOrganization.getSelectedIndex())).equals("Visitor Organization")){
        nameJTextField.setEnabled(false);
        nameJTextField.setEditable(false);
        jPasswordFieldPassword.setEnabled(false);
        jPasswordFieldPassword.setEditable(false);
        }
        else{
        nameJTextField.setEnabled(true);
        nameJTextField.setEditable(true);
        
        jPasswordFieldPassword.setEnabled(true);
        jPasswordFieldPassword.setEditable(true);
        }
        
        
        
        
    }//GEN-LAST:event_JComboBoxOrganizationActionPerformed

    private void btnCreateUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateUserActionPerformed
        String userName = nameJTextField.getText();
        String password = String.valueOf(jPasswordFieldPassword.getPassword());

        if( password.equals("") && userName.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter values in all the fields");
            return;
        }

        Organization organization = (Organization) JComboBoxOrganization.getSelectedItem();
        Employee employee = (Employee) JComboBoxEmployee.getSelectedItem();
        Role role = (Role) JComboBoxRole.getSelectedItem();
        
        

        if(!RegularExpressionCheck.isValidEmailAddress(userName)){
            JOptionPane.showMessageDialog(null, " Invalid Username." +
                "Username should be an email-ID with '_' and '@' as the only allowed special characters but should not start with '_'");
            return;
        }
        
        if(!RegularExpressionCheck.isValidPassword(password)){
            JOptionPane.showMessageDialog(null, " Invalid Password." + 
                "Password should contain atleast one lower case, one upper case, one digit, one special character & length should be minimum 6 to max 16 letters");
            return;
        }
        Account account = organization.getUserAccountDirectory().createUserAccount(userName, password, employee, role);

        if(account == null){
             JOptionPane.showMessageDialog(null, "User already exists, please try with another username.");
            clearFields();
            return;
        }

        clearFields();
        popData();
    }//GEN-LAST:event_btnCreateUserActionPerformed

